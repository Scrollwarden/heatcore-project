class BaseModel:
    def __init__(self, app, vao_name, tex_id, pos=(0, 0, 0), rot=(0, 0, 0), scale=(1, 1, 1)):
        self.app = app
        self.pos = pos
        self.vao_name = vao_name
        self.rot = glm.vec3([glm.radians(a) for a in rot])
        self.scale = scale
        self.m_model = self.get_model_matrix()
        self.tex_id = tex_id
        self.vao = app.mesh.vao.vaos[vao_name]
        self.program = self.vao.program
        self.camera = self.app.camera

    def update(self): ...

    def get_model_matrix(self):
        m_model = glm.mat4()
        # translate
        m_model = glm.translate(m_model, self.pos)
        # rotate
        m_model = glm.rotate(m_model, self.rot.z, glm.vec3(0, 0, 1))
        m_model = glm.rotate(m_model, self.rot.y, glm.vec3(0, 1, 0))
        m_model = glm.rotate(m_model, self.rot.x, glm.vec3(1, 0, 0))
        # scale
        m_model = glm.scale(m_model, self.scale)
        return m_model

    def render(self):
        self.update()
        self.vao.render()



class AdvancedSkyBox(BaseMesh):
    def __init__(self, app, vao_name='advanced_skybox', texture_path='skybox',
                 pos=(0, 0, 0), rot=(0, 0, 0), scale=(1, 1, 1)):
        super().__init__()
        self.app = app
        self.context = app.context
        self.vbo_format = "3f"
        self.attrs = ["in_position"]

        self.texture_path = texture_path
        self.texture = None
        self.init_texture_cube()
        self.camera = app.planet.camera

    def init_shader(self):
        self.shader_program = open_shaders(self.app, "advanced_skybox")
        self.shader_program['u_texture_skybox'] = 0
        self.texture.use(location=0)

    def init_vertex_data(self):
        z = 0.9999 # Clipping space
        self.vertex_data = np.array([(-1, -1, z), (3, -1, z), (-1, 3, z)], dtype='f4')

    def init_texture_cube(self, ext='png'):
        faces = ['right', 'left', 'top', 'bottom'] + ['front', 'back'][::-1]
        # textures = [pg.image.load(dir_path + f'{face}.{ext}').convert() for face in faces]
        textures = []
        for face in faces:
            texture = pg.image.load(self.texture_path + f'{face}.{ext}').convert()
            if face in ['right', 'left', 'front', 'back']:
                texture = pg.transform.flip(texture, flip_x=True, flip_y=False)
            else:
                texture = pg.transform.flip(texture, flip_x=False, flip_y=True)
            textures.append(texture)

        size = textures[0].get_size()
        self.texture = self.context.texture_cube(size=size, components=3, data=None)

        for i in range(6):
            texture_data = pg.image.tostring(textures[i], 'RGB')
            self.texture.write(face=i, data=texture_data)

    def update(self):
        m_view = glm.mat4(glm.mat3(self.camera.view_matrix))
        self.shader_program['m_invProjView'].write(glm.inverse(self.camera.m_proj * m_view))


